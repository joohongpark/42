int				ft_is_sort(int *tab, int length, int (*f)(int, int))
{
	int			i;
	int			s[2];

	if (length < 2)
		return (1);
	i = 1;
	s[0] = 0;
	s[1] = 0;
	while (i < length)
	{
		if ((f)(tab[i - 1], tab[i]) > 0)
			s[0]++;
		if ((f)(tab[i - 1], tab[i]) < 0)
			s[1]++;
		i++;
	}
	if (s[0] > 0 && s[1] > 0)
		return (0);
	else
		return (1);
}
