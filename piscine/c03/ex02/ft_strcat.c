char		*ft_strcat(char *dest, char *src)
{
	char	*rtn;
	int		p;

	p = 0;
	rtn = dest;
	while (*dest != '\0')
		dest++;
	while (src[p] != '\0')
	{
		dest[p] = src[p];
		p++;
	}
	dest[p] = '\0';
	return (rtn);
}
