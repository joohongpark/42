int		ft_atoi(char *str)
{
	int sum;
	int p;

	sum = 0;
	p = 1;
	while ((*str == '\t')
		|| (*str == '\n')
		|| (*str == '\v')
		|| (*str == '\f')
		|| (*str == '\r')
		|| (*str == ' '))
		str++;
	while (*str == '+' || *str == '-')
		p = (*str++ == '-') ? (p * -1) : p;
	while (*str >= '0' && *str <= '9')
		sum = (*str++ - '0') + sum * 10;
	return (sum * p);
}
