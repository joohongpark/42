char		*ft_strcapitalize(char *str)
{
	char	*ptr;

	ptr = str;
	while (*str != '\0')
	{
		if (*str >= 'A' && *str <= 'Z')
		{
			if ((*(str - 1) >= 'a' && *(str - 1) <= 'z')
				|| (*(str - 1) >= 'A' && *(str - 1) <= 'Z')
				|| (*(str - 1) >= '0' && *(str - 1) <= '9'))
				*str = *str + 32;
		}
		if (*str >= 'a' && *str <= 'z')
		{
			if (((*(str - 1) < 'a' || *(str - 1) > 'z')
				&& (*(str - 1) < 'A' || *(str - 1) > 'Z')
				&& (*(str - 1) < '0' || *(str - 1) > '9'))
				|| (ptr == str))
				*str = *str - 32;
		}
		str++;
	}
	return (ptr);
}
