#include <unistd.h>

void		ft_putstr_non_printable(char *str)
{
	char	p[3];

	p[0] = '\\';
	while (*str != '\0')
	{
		if (!(*str >= 32 && *str < 127))
		{
			p[1] = (unsigned char)*str >> 4;
			p[1] = (p[1] <= 9) ? (p[1] + '0') : (p[1] - 10 + 'a');
			p[2] = (unsigned char)*str & 0x0f;
			p[2] = (p[2] <= 9) ? (p[2] + '0') : (p[2] - 10 + 'a');
			write(1, p, 3);
		}
		else
		{
			write(1, str, 1);
		}
		str++;
	}
}
